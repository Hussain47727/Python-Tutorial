  # RANGE 

print(range(10))

 # IF WE WANT TO PRINT 0 TO 9
for i in range(10):
    print(i)
# IF WE WANT TO PRINT 2 , 9
for i in range(2 , 10):
    print(i)

# IF WE WANT TO PRINT EVEN NUMBER OR MULTIPLE OF 2
for i in range(2, 10, 2):
    print(i)

